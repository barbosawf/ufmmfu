#' Plots ggplo2-made graphics from `nlme::nlsList()` objects.
#'
#' @param df Data frame used to fit the non-linear models by the `nlme::nlsList()` function.
#' @param x.axis Independent variable used to the fit non-linear models. It must be contained in the data frame.
#' @param y.axis Dependent variable used to the fit non-linear models. It must be contained in the data frame.
#' @param grouping.var Variable that defines the data grouping. Example: `y ~ x | grouping.var`.
#' @param fun Modified non-linear function with `.args` prefix (required for internal computation) and suffixes that refer to the parameters of the non-linear model used.
#' Example: if the logistic model was used as `function(x, Asym, xmid, scal) {Asym / (1 + exp(-scal * (x - xmid)))}` in the `nlme::nlsList()` function, the argument `fun` will be `function(x, .args) {.args$Asym / (1 + exp(-.args$scal * (x - .args$xmid)))}`.
#' @param nlsList.obj Object returned using the `nlme::nlsList()` function.
#' @param color Sets the color of the plotted estimated non-linear curves.
#' @param linewidth Sets the line width of the plotted estimated non-linear curves.
#' @param x.lab Sets the x-axis label. Default is `NULL` and if `NULL` returns the value of the x.axis argument.
#' @param y.lab Sets the y-axis label. Default is `NULL` and if `NULL` returns the value of the y.axis argument.
#'
#' @returns Return a list and a panel of ggplo2-made graphics from the object generated by the `nlme::nlsList()` function. Non-linear curves are smoothed by using the `ggplot2::stat_function()` function.
#'
#' @export
#'
#' @examples
#'
#' df <- datasets::Orange
#'
#' logistic <- function(x, Asym, xmid, scal){Asym / (1 + exp(- scal * (x - xmid)))}
#'
#' nlsList.obj <- nls(circumference ~ logistic(age, Asym, xmid, scal),
#' start = list(Asym = 170, xmid = 600, scal = .004),
#' data = df)
#'
#'
#' x.axis <- "age"
#' y.axis <- "circumference"
#' grouping.var <- "Tree"
#' fun <- function(x, .args) {.args$Asym / (1 + exp(-.args$scal * (x - .args$xmid)))}
#' x.lab <- "Age (days since 1968/12/31)"
#' y.lab <- "Trunk circumference (mm)"
#'
#'
#' nlsList_plot(df = df,
#'              x.axis = x.axis,
#'              y.axis = y.axis,
#'              grouping.var = grouping.var,
#'              fun = fun,
#'              nlsList.obj = nlsList.obj,
#'              color = 'blue',
#'              linewidth = 0.5,
#'              x.lab = x.lab,
#'              y.lab = y.lab) -> g
#'
#' g$list_p
#'
#' g$panel_p
#'
nlsList_plot <-
  function(df,
           x.axis,
           y.axis,
           grouping.var,
           fun,
           nlsList.obj,
           color = 'blue',
           linewidth = 0.5,
           x.lab = NULL,
           y.lab = NULL
  ) {


    # grouping.var is turned into symbol to be used in nest_by function
    g.var <- sym(grouping.var)

    ordem <- sort(levels(df[[grouping.var]]))

    df |>
      mutate_at(vars({{grouping.var}}),
                ~ factor(., levels = ordem)) |>
      arrange_at(grouping.var) -> df


    coef(nlsList.obj) |>
      rownames_to_column(var = grouping.var) |>
      mutate_at(vars({{grouping.var}}),
                ~ factor(., levels = ordem)) |>
      arrange_at(grouping.var) -> arr.nlsList.obj


    arr.nlsList.obj |>
      na.omit() |>
      nest_by({{g.var}}, .keep = T) -> nested_args


    args_list <- nested_args$data


    args_list |>
      setNames(nested_args[[grouping.var]]) -> args_list


    arr.nlsList.obj |>
      apply(1, anyNA) -> id_with_nas

    arr.nlsList.obj |>
      dplyr::select({{grouping.var}}) |> pull() -> gv

    id_with_nas <- as.vector(droplevels(gv[id_with_nas]))


    x_min_max <- c(min = min(df[[x.axis]]), max = max(df[[x.axis]]))

    fit_values <- as.vector(na.omit(fitted(nlsList.obj)))
    y_min_max <- c(min = min(c(fit_values, df[[y.axis]])),
                   max = max(c(fit_values, df[[y.axis]])))


    filtered_df <-
      df |> filter_at(vars(all_of(grouping.var)), ~ !. %in% id_with_nas)


    nested_df <- filtered_df |>
      nest_by({{g.var}}, .keep = T)

    df_list <- nested_df$data |>
      setNames(nested_df[[1]])

    df_list |>
      map(~.x |>
            summarise_at(vars({{x.axis}}),
                         list(min, max))) -> lms

    # x.axis and y.axis are turned into symbol to be used in geom_point function
    x.axis <- sym(x.axis)
    y.axis <- sym(y.axis)



    f.x <- function(x.lab) {
      if (is.null(x.lab)) {
        # A waiver is a "flag" object to indicate the calling function
        # should just use the default value.
        xlab(waiver())
      } else {
        xlab(x.lab)
      }
    }

    f.y <- function(y.lab) {
      if (is.null(y.lab)) {
        ylab(waiver())
      } else {
        ylab(y.lab)
      }
    }


    # the pmap function is needed to map objects into three lists simultaneously
    # df_list contain a list of data separated by the grouping.var argument
    # therefore df_list is used in the geom_point function
    # args_list contains the list of parameters estimated by the nlsList function
    # therefore args_list is used in the stat_function function
    # lms is a list with the limits of the x-axis where the non-linear function is applied


    pmap(list(df_list, args_list, lms), \(.df, .args, .lms) {
      ggplot() +
        geom_point(aes(x = {{x.axis}}, y = {{y.axis}}), data = .df) +
        stat_function(
          fun = \(x) fun(x, .args),
          color = color,
          xlim = c(.lms[[1]], .lms[[2]]),
          linewidth = linewidth
        ) +
        scale_x_continuous(limits = x_min_max) +
        scale_y_continuous(limits = y_min_max) +
        labs(title = .args[[grouping.var]]) +
        theme_minimal() +
        theme(
          plot.title = element_text(
            size = 10,
            hjust = 0.5,
            vjust = -0.5
          )
        )  +
        f.x(x.lab) +
        f.y(y.lab)

    }) -> list_p


    ggarrange(
      plotlist = list_p |>
        map( ~ . + rremove('xlab') +
               rremove('ylab'))
    ) |>
      annotate_figure(
        left = text_grob(
          ifelse(is.null(y.lab), y.axis, y.lab),
          rot = 90,
          vjust = 0.5,
          size = 14
        ),
        bottom = text_grob(ifelse(is.null(x.lab), x.axis, x.lab),
                          size = 14)
      ) -> panel_p

    return(list(list_p = list_p, panel_p = panel_p))


  }
